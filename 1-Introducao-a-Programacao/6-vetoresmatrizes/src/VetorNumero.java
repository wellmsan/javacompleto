public class VetorNumero {
    public static void main(String[] args) {
        int[] numeros = {10, 20, 30, 40, 50};
        System.out.println("Primeiro número: " + numeros[0]);

        for(int i = 0; i < numeros.length; i++){
            System.out.println("Posição " + i + " " + numeros[i]);
        }

    }
}
